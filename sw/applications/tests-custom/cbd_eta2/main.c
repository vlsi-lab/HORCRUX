/* 
 * For each j=0..7:
 *   a = (d >> (4*j + 0)) & 0x3;
 *   b = (d >> (4*j + 2)) & 0x3;
 *   out[j] = a - b;
 */
void cbd_eta2(int32_t d, int8_t out[NTESTS][NLANES], int i) {
    for (int j = 0; j < NLANES; j++) {
        int32_t a = (d >> (4 * j + 0)) & 0x3;
        //printf("a = %d\n", a);
        int32_t b = (d >> (4 * j + 2)) & 0x3;
        //printf("b = %d\n", b);
        out[i][j] = (int8_t)(a - b);
    }
}

