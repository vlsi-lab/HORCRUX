void ntt_hw(int32_t a[N]) {
  unsigned int len, start, j, k;
  int32_t zeta, t;
  uint64_t prod;      // raw product
  uint32_t temp2;

  k = 0;
  for(len = 128; len > 0; len >>= 1) {
    for(start = 0; start < N; start = j + len) {
      zeta = zetas[++k];
      for(j = start; j < start + len; ++j) {

        int32_t aj   = a[j];
        int32_t alen = a[j + len];
        int32_t t, rlo, rhi;

        asm volatile(
            "mul    %[rlo], %[z], %[al]\n\t"
            "mulh   %[rhi], %[z], %[al]\n\t"
            "mv     a3,     %[rlo]\n\t"
            ".insn r 0x3b, 0x7, 0x4, %[t], a3, %[rhi]\n\t"
            "sub    %[al],  %[aj], %[t]\n\t"   // alen = aj - t
            "add    %[aj],  %[aj], %[t]\n\t"   // aj   = aj + t
            : [aj] "+r"(aj), [al] "+r"(alen),
              [t]  "=&r"(t), [rlo] "=&r"(rlo), [rhi] "=&r"(rhi)
            : [z] "r"(zeta)
            : "cc", "a2", "a3"
        );
        a[j]       = aj;
        a[j + len] = alen;

      }
    }
  }
}

