static inline int32_t montgomery_reduce_dsa(int64_t a) {
  int32_t t;
  t = (int64_t)(int32_t)a * DSA_QINV;
  t = (a - (int64_t)t * DSA_Q) >> 32;
  return t;
}
   
void invntt_dilithium(int32_t a[N]) {
  unsigned int start, len, j, k;
  int32_t t, zeta;
  const int32_t f = 41978; // mont^2/256

  k = 256;
  for(len = 1; len < N; len <<= 1) {
    for(start = 0; start < N; start = j + len) {
      zeta = -zetas[--k];
      for(j = start; j < start + len; ++j) {
        t = a[j];
        a[j] = t + a[j + len];
        a[j + len] = t - a[j + len];
        a[j + len] = montgomery_reduce_dsa((int64_t)zeta * a[j + len]);
      }
    }
  }

  for(j = 0; j < N; ++j) {
    a[j] = montgomery_reduce_dsa((int64_t)f * a[j]);
  }
}



