// Carryless 8x8 → 16 (split in l,h) with simple bit-slice logic
static inline void gf_carryless_mul_simple(uint8_t *c, uint8_t a, uint8_t b) {
    uint16_t l = 0, h = 0;

    uint16_t u1 = (uint16_t)(b & 0x7F); // low 7 bits
    uint16_t u2 = (uint16_t)(u1 << 1);  // u1 shifted

    // c = 0,2,4,6  (four 2-bit slices)
    uint16_t t0 = (a     ) & 0x3;
    uint16_t g0 = ((-(t0 & 1)) & u1) ^ ((-((t0 >> 1) & 1)) & u2);
    l ^= (g0 << 0);
    h ^= (g0 >> 8); // (>> (8-0))

    uint16_t t1 = (a >> 2) & 0x3;
    uint16_t g1 = ((-(t1 & 1)) & u1) ^ ((-((t1 >> 1) & 1)) & u2);
    l ^= (g1 << 2);
    h ^= (g1 >> 6);

    uint16_t t2 = (a >> 4) & 0x3;
    uint16_t g2 = ((-(t2 & 1)) & u1) ^ ((-((t2 >> 1) & 1)) & u2);
    l ^= (g2 << 4);
    h ^= (g2 >> 4);

    uint16_t t3 = (a >> 6) & 0x3;
    uint16_t g3 = ((-(t3 & 1)) & u1) ^ ((-((t3 >> 1) & 1)) & u2);
    l ^= (g3 << 6);
    h ^= (g3 >> 2);

    // Handle b's MSB (bit 7) once
    uint16_t mask = (uint16_t)(-(int)((b >> 7) & 1));
    l ^= ((uint16_t)a << 7) & mask;
    h ^= ((uint16_t)a >> 1) & mask;

    c[0] = (uint8_t)l;
    c[1] = (uint8_t)h;
}

/************** Original oode ****************************/
void gf_carryless_mul(uint8_t *c, uint8_t a, uint8_t b) {
    uint16_t       h = 0, l = 0, g, u[4];
    u[0] = 0;
    u[1] = b & ((1UL << 7) - 1UL);
    u[2] = u[1] << 1;
    u[3] = u[2] ^ u[1];
  
    g = 0;
    uint16_t tmp1 = a & 3;
  
    for(int i =0; i < 4; i++) {
      uint32_t tmp2 = tmp1 - i;
      g ^= (u[i] & -(1 - ((tmp2 | -tmp2) >> 31)));
    }
  
    l = g;
    h = 0;
  
    for (uint8_t i = 2; i < 8; i+=2) {
      g = 0;
      uint16_t tmp1 = (a >> i) & 3;
      for (int j = 0; j < 4; ++j) {
        uint32_t tmp2 = tmp1 - j;
        g ^= (u[j] & -(1 - ((tmp2 | -tmp2) >> 31)));
      }
      
      l ^= g << i;
      h ^= g >> (8 - i);
    }
  
    uint16_t mask = (-((b >> 7) & 1));
    l ^= ((a << 7) & mask);
    h ^= ((a >> (1)) & mask);
  
    c[0] = l;
    c[1] = h;
  
}


void gf_carryless_mul_HW(uint8_t *c, uint8_t a, uint8_t b) {

    uint32_t rd;
    // rd[15:0] = l, rd[31:16] = h
    asm volatile (
        ".insn r 0x3b, 0x1, 7, %[dst], %[rs1], %[rs2]\n\t"
        : [dst] "=&r" (rd)
        : [rs1] "r" (a), [rs2] "r" (b)
        : "cc"
    );

    c[0] = (uint8_t)(rd & 0xFF);
    c[1] = (uint8_t)((rd >> 16) & 0xFF); // h low byte
}

