int32_t montgomery_reduce(int64_t a) {

    uint32_t lo = (uint32_t)a;
    uint32_t t  = (lo << 25)
                + (lo << 24)
                + (lo << 23)
                + (lo << 13)
                +  lo;            // all adds/wrap mod 2^32

    // 2) m = t * Q  via shifts: Q = (1<<23) - (1<<13) + 1
    int64_t m = ((int64_t)t << 23)
              - ((int64_t)t << 13)
              +  (int64_t)t;

    int32_t result = (int32_t)((a - m) >> 32);

    return result;
}
   
void invntt_dilithium(int32_t a[N]) {
  unsigned int start, len, j, k;
  int32_t t, zeta;
  const int32_t f = 41978; // mont^2/256

  k = 256;
  for(len = 1; len < N; len <<= 1) {
    for(start = 0; start < N; start = j + len) {
      zeta = -zetas[--k];
      for(j = start; j < start + len; ++j) {
        t = a[j];
        a[j] = t + a[j + len];
        a[j + len] = t - a[j + len];
        a[j + len] = montgomery_reduce((int64_t)zeta * a[j + len]);
      }
    }
  }

  for(j = 0; j < N; ++j) {
    a[j] = montgomery_reduce((int64_t)f * a[j]);
  }
}


void invntt_dilithium_hw(int32_t a[N]) {
  unsigned int start, len, j, k;
  int32_t t, zeta;
  const int32_t f = 41978; // mont^2/256
  int64_t prod;
  uint32_t temp2;

  k = 256;
  for(len = 1; len < N; len <<= 1) {
    for(start = 0; start < N; start = j + len) {
      zeta = -zetas[--k];
      for(j = start; j < start + len; ++j) {

        int32_t aj   = a[j];
        int32_t alen = a[j + len];
        int32_t t, rlo, rhi;

        asm volatile(
            "mv     %[t],   %[aj]\n\t"
            "add    %[aj],  %[aj],  %[al]\n\t"
            "sub    %[al],  %[t],   %[al]\n\t"
            "mul    %[rlo], %[z],   %[al]\n\t"
            "mulh   %[rhi], %[z],   %[al]\n\t"
            "mv     a3,     %[rlo]\n\t"
            ".insn r 0x3b, 0x1, 0x6, %[al], a3, %[rhi]\n\t"
            : [aj] "+r"(aj), [al] "+r"(alen), [t]  "=&r"(t), [rlo] "=&r"(rlo), [rhi] "=&r"(rhi)
            : [z] "r"(zeta)
            : "cc", "a3"
        );
        a[j]       = aj;
        a[j + len] = alen;
  
      }
    }
  }

  for(j = 0; j < N; ++j) {
    int32_t aj  = a[j];
    int32_t rlo, rhi;

    asm volatile(
        "mul    %[rlo], %[f],  %[aj]\n\t"          // low word of f*aj
        "mulh   %[rhi], %[f],  %[aj]\n\t"          // signed high word
        "mv     a3,     %[rlo]\n\t"                // reducer expects low in a3
        ".insn r 0x3b, 0x1, 0x6, %[aj], a3, %[rhi]\n\t"  // aj <- REDUCE(a3, rhi)
        : [aj] "+r"(aj), [rlo] "=&r"(rlo), [rhi] "=&r"(rhi)
        : [f] "r"((int32_t)f)
        : "cc", "a2", "a3"
    );
    a[j] = aj;
  }
}

