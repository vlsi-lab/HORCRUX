// eta = 1  → per lane: [b:a] uses 2 bits (a at 2*j+0, b at 2*j+1)
void cbd_eta1(int32_t d, int8_t out[NTESTS][NLANES], int i) {
    for (int j = 0; j < NLANES; j++) {
        int32_t a = (d >> (2 * j + 0)) & 0x1;
        int32_t b = (d >> (2 * j + 1)) & 0x1;
        out[i][j] = (int8_t)(a - b);
    }
}


